# 1️⃣ БАЗОВАЯ КОНСТРУКЦИЯ
temperature = 22

if temperature > 30:
    print("Жарко! Включим кондиционер ❄️")
elif temperature < 15:
    print("Холодно! Включим обогреватель 🔥")
else:
    print("Комфортно. Ничего менять не нужно ✅")

# 🔍 Почему так?
# 1. Python проверяет условия СВЕРХУ ВНИЗ.
# 2. Как только нашёл первое True → выполняет блок → ИГНОРИРУЕТ остальные.
# 3. else — это "запасной вариант", если ни одно условие не подошло.
# 4. Отступы (4 пробела) ОБЯЗАТЕЛЬНЫ. Без них код не запустится.

# 2️⃣ ЛОГИЧЕСКИЕ ОПЕРАТОРЫ
age = 20
has_ticket = True

if age >= 18 and has_ticket:
    print("Доступ разрешён 🎟️")
elif age < 18:
    print("Доступ запрещён: требуется 18+ 🚫")
else:
    print("Доступ запрещён: нет билета 🚫")

