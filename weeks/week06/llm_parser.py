# Задача: LLM вернул строку вида: "Результат поиска: [ссылкка1, ссылка2, ссылка3] !"
# 1. Убрать эмодзи и пробелы по краям (strip)
# 2. Заменить "Результат поиска:" и ":)" на пустую строку
# 3. Разбить оставшуюся строку по запятой -> получить список ссылок
# 4. Убрать пробелы у каждой ссылки (метод strip() + цикл или map)
# 5. Вернуть чистый список

def parse_llm_links(raw):
    cleaned = raw.strip()         # 1. Очистить края
    cleaned = cleaned.replace("Результаты поиска:", "").replace("!", "")  # 2. Убрать префикс и суффикс
    parts = cleaned.split(",")    # 3. Разбить по запятой
    links = [link.strip() for link in parts]
    return links

raw = "Результат поиска: [url1, url2, url3] !"
print(parse_llm_links(raw))
