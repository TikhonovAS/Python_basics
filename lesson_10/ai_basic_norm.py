# Учебная версия ИИ-агента без реальных запросов к сети.
# Позволяет отработать архитектуру, классы и логику без траты денег и ключей.

import time                          # Для имитации задержки мышления
import random                        # Для случайного выбора ответа

# КЛАСС: Заглушка ИИ-агента
class MockAIAgent:
  """
  Имитация ИИ для обучения (Вместро реального запроса к серверу возвращает
  случайный ответ из списка.)
  """
  # 1. КОНСРУКТОР: настраивает нового агента при создании
  def __init__(self, name="MockBot"):
    """Создает агента с именем и пустой историей."""
    self.name = name          # Атрибут: имя агента
    self.history = []         # Атрибут: список для хранения диалога
    print(f"Агент '{self.name}' инициализирован и готов к работе.")

  # 2. МЕТОД: Запрос к ИИ (основная логика)
  def ask(self, question):
    """
    Принимает вопрос, имитирует 'мышление' и возвращает ответ.
    Также сохраняет вопрос и ответ в историю.
    """
    # Имитация сетевой задержки (чтобы было как в реальном чате)
    print(f"{self.name} думает над вопросом...")
    time.sleep(2)                  # Пауза 2 сек.

    # База знаний: список возможных ответов (упрощена)
    possible_answers = [
        f"Рекурсия - это когда функция вызывает саму себя, как матрешку.",
        "Рекурсия требует базового случая (условия выхода), иначе будет бесконечный цикл.",
        "Представь зеркало напротив зеркала - это визуальная рекурсия.",
        "В программировании рекурсия - это альтернатива циклам для решения задач."
    ]

    # Случайным образом выбираем один ответ из списка
    answer = random.choice(possible_answers)

    # Сохраняем диалог в историю (список словарей)
    # Формат как в реальных API: role (кто говорит) + content (текст)
    self.history.append({"role": "user", "content": question})
    self.history.append({"role": "assistant", "content": answer})

    # Возвращаем ответ вызывающему коду
    return answer

  # 3. МЕТОД: Просмотр истории
  # ВАЖНО!!! Этот метод должен быть ВНУТРИ класса (с отступом)!
  def get_history(self):
    """Возвращает всю историю переписки."""
    return self.history

  # 4. МЕТОД: Очистка памяти
  def clear_history(self):
    """Полностью очищает историю диалога"""
    self.history = []
    print(f"{self.name} очистил память.")


# ***** ОСНОВНАЯ ПРОГРАММА ***** (Точка входа)
# Эта конструкция гарантирует, что код запустится толька если файл открыт напрямую
# а не импрортирован как модуль в другом месте.
if __name__ == "__main__":

  # 1. Создаем экземпляр агента (вызывается __init__)
  my_bot = MockAIAgent("Умник")

  print("\n--- ТЕСТ 1: Обычный вопрос ---")
  # 2. Задаем вопрос и получаем ответ
  response = my_bot.ask("Объясни, что такое рекурсия, в одном предложении.")
  print(f"Ответ бота: {response}")

  print("\n--- ТЕСТ 2: Проверка истории ---")
  # 3. Смотрим, что сохранилось в памяти
  # get_history() возвращает список, печатает его построчно для красоты
  for msg in my_bot.get_history():
    print(f"[{msg['role']}]: {msg['content']}")

  print("\n--- ТЕСТ 3: Второй вопрос ---")
  # 4. Задаем еще один вопрос, чтобы проверить накопление истории
  my_bot.ask("А что такое цикл while?")

  print('\n--- ТЕСТ 4: Финальная история ---')
  # 5. Печатаем всю историю (теперь там 4 сообщения: 2 вопроса + 2 ответа)
  print(f"Всего сообщений в памяти: {len(my_bot.get_history())}")

  print("\n--- ТЕСТ 5: Очистка ---")
  # 6. Очищаем память и проверяем
  my_bot.clear_history()
  print(f"Сообщений после очистки: {len(my_bot.get_history())}")