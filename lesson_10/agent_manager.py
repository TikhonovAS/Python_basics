# Менеджер ИИ-агентов с логированием, декораторами и экпортом в JSON

import json
import time
import random
from datetime import datetime


# -----------------------------------------------
# 1. ВСПОМОГАТЕЛЬНЫЕ ФУНКЦИИ
# -----------------------------------------------
def log_action(message, filename="agent_1.log"):
    """Безопасная запись в лог-файл"""
    try:
        with open(filename, "a", encoding="utf-8") as f:
            # Формирует текущее время в часы:минуты:секунды
            timestamp = datetime.now().strftime("%H:%M:%S")

            # Записываем строку вида: [14:32:10] Сообщение\n
            f.write(f"[{timestamp}] {message}\n")

    except Exception as e:
        # Обработка ошибок: если нет прав на запись или диск полный,
        # программа не упадет, а просто напечатает предупреждение в консоль.
        print(f"Ошибка записи лога: {e}")


# -----------------------------------------------------------------------
# 2. ДЕКОРАТОР: автоматическое логирование вызовов методов
# -----------------------------------------------------------------------
def track_call(func):
    """
    Декоратор, логирует ВХОД в метод, замеряет время выполнения,
    логирует ВЫХОД и результат. Не меняет исходный код метода!
    """

    # wrapper - это новая функция, которая ЗАМЕНЯЕТ оригинальную функцию func.
    # self - обязательный первый аргумент для методов класса.
    # *args, **kwargs - ловят любые остальные аргументы, чтобы декоратор был универсальным.
    def wrapper(self, *args, **kwargs):
        """
        ЛОГ ВХОДА: фиксируем, какой метод вызвали и с какими параментрами
        """
        log_action(f"ВЫЗОВ: {func.__name__}(args={args}, kwargs={kwargs})")

        # ЗАСЕКАЕМ ВРЕМЯ СТАРТА (в сек. с эпохи Unix)
        start_time = time.time()

        # ВЫЗЫВАЕМ ИСХОДНЫЙ МЕТОД:
        # Пробразываем self и все аргументы внутрь оригинальной функции.
        # Результат сохраняем, чтобы потом вернуть его вызывающему коду.
        result = func(self, *args, **kwargs)

        # СЧИТАЕМ ДЛИТЕЛЬНОСТЬ: текущее время минус время старта
        duration = time.time() - start_time

        # ЛОГ ВЫХОДА: фиксируем завершение, время работы и обрезанный результат
        # str(result)[:50] ... --> берем первые 50 символов, чтобы лог не раздувался
        log_action(f"ЗАВЕРШЕНО: {func.__name__} за {duration:.2f} сек. | Результат: {str(result)[:50]}...")

        # ВОЗВРАЩАЕМ РЕЗУЛЬТАТ:
        # Без этой строки метод вернул бы None даже если внутри что-то посчитал.
        return result

    # Возвращаем обертку. Python автоматически подставит ее вместо func.
    return wrapper


