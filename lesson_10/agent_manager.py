# 4. КЛАСС: Менеджер агентов (AgentManager)
# --------------------------------------------
class AgentManager:
    """
    Диспетчерская система. Хранит пул агентов, распределяет задачи,
    управляет сессиями и экспортирует данные в файлы.
    """

    def __init__(self):
        """При создании менеджера инициализируем пустой список агентов"""
        self.agents = []
        log_action("МЕНЕДЖЕР ИНИЦИАЛИЗИРОВАН")
        print("Менеджер запущен. Пул агентов пуст.")

    @track_call
    def add_agent(self, agent):
        """Добавляет готового агента в список управления"""
        self.agents.append(agent)  # Добавляет объект в конец списка
        log_action(f"Добавлен агент: {agent.name}")
        print(f"{agent.name} успешно добавлен в пул.")

    @track_call
    def run_session(self, agent_name, question):
        """
        Находит агента по имени и задает ему вопрос.
        Безопасный поиск без try/except благодаря next() с значением по умолчанию
        """
        agent = next((a for a in self.agents if a.name == agent_name), None)

        if not agent:  # Если agent == None --> значит, такого имени нет в списке
            print(f"Агент '{agent_name}' не нейден в пуле!")
            return None
        print(f"\nВопрос для {agent.name}: \"{question}\"")
        return agent.ask(question)  # вызывает метод ask() у найденного объекта

    @track_call
    def export_history(self, filename="history.json"):
        """
        Собирает историю и статистику ВСЕХ агентов и сохраняет в один JSON-файл.
        Формат файла продуман для легкого чтения человеком и парсинга машиной.
        """
        # Готовим структуру данных (словарь Python)
        export_data = {
            "export_date": datetime.now().isoformat(),  # ISO-формат даты: 2024-05-31Т15:40:12.123456
            "agents": []  # сюда сложим данные по каждому агенту
        }

        # Проходим по всем агентам в пуле
        for agent in self.agents:
            export_data["agents"].append({
                "name": agent.name,
                "history": agent.get_history(),  # Копируем всю переписку
                "stats": agent.get_stats()  # Копируем статистику
            })

        # ЗАПИСЬ В ФАЙЛ:
        try:
            # "w" (write) --> ОТКРЫВАЕТ НА ПЕРЕЗАПИСЬ. Старый файл сотрется и запишется новый.
            with open(filename, "w", encoding="utf-8") as f:
                # json.dump() превращает Python-словарь в JSON-текст и сразу пишем в файл.
                # indent-2 --> добавляет отступы для читаемости человеком
                # ensure_ascii=False --> сохраняет кириллицу как есть.
                json.dump(export_data, f, indent=2, ensure_ascii=False)

            log_action(f"История экспортирована в {filename}")
            print(f"Данные успешно сохранены в {filename}")

        except Exception as e:
            log_action(f"Ошибка экспорта JSON: {e}")
            print(f"Не удалось сохранить файл: {e}")


# ---------------------------------------------
# 5. ТОЧКА ВХОДА: тестовый сценарий запуска
# ---------------------------------------------
if __name__ == "__main__":
    """
    Этот блок выполнится ТОЛЬКО если запустить файл напрямую: python agent_manager.py
    Если импортировать этот файл в другом скрипте --> блок проигнорируется
    Это стандарт защиты от случайного запуска при импорте.
    """
    print("Запуск Менеджера ИИ-агентов...\n" + "=" * 50)

    # 1. Создаем диспетчера
    manager = AgentManager()

    # 2. Создаем агентов(вызывается __init__ каждого класса)
    bot_python = MockAIAgent("Python-Expert")
    bot_math = MockAIAgent("Math-Tutor")

    # 3. Регистрируем их в менеджере
    manager.add_agent(bot_python)
    manager.add_agent(bot_math)

    # 4. Импортируем рабочие сессии
    manager.run_session("Python-Expert", "Что такое рекурсия?")
    manager.run_session("Math-Tutor", "Как посчитать площадь круга?")
    manager.run_session("Python-Expert", "Чем список отличается от кортежа?")

    # 5. Проверяем, что происходит при обращении к несуществующему агенту
    manager.run_session("Ghost_Bot", "Тут есть кто-нибудь?")  # Выведет ошибку

    # 6. Экспортируем все накопленное в файл
    print("\n" + "=" * 50)
    print("Начинаем экспорт истории в JSON...")
    manager.export_history()

    print("\nТест завершен!")
    print("Проверь файлы в папке:")
    print("   agent.log --> аудит вызовов, время, статусы")
    print("   history.json --> полная версия диалогов в структурированном виде")
