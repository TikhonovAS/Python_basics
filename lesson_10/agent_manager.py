# 3. КЛАСС: имитация ИИ-агента (MockAIAgent)
# ----------------------------------------------------------------
class MockAIAgent:
    """
    Учебная заглушка ИИ. Вместо реального запроса к API возвращает случайный ответ.
    Хранит историю диалога в формате, совместимым с реальными нейросетями.
    """


    def __init__(self, name):
        """
        Конструктор: вызывается автоматически при создании объекта: bot = MockAIAgent("Имя")
        """
        self.name = name  # Атрибут: имя агента
        self.history = []  # Атрибут: пустой список для хранения сообщений
        log_action(f"АГЕНТ СОЗДАН: {name}")  # Логируем факт создания
        print(f"Агент '{self.name}' готов к работе!")


    @track_call
    def ask(self, question):
        """
        Принимает вопрос пользователя, имитирует 'мышление', возвращает ответ.
        Паралельно сохраняет диалог в self.history
        """
        print(f"{self.name} думает ...")
        time.sleep(3)  # имитация задержки сети/вычислений (3 сек.)

        # База знаний: список заготовленных ответов
        answers = [
            "Рекурсия - это когда функция вызывает саму себя, как матрешка."
            "Цикл while повторяет блок кода, пока условие остается истинным."
            "Список в Python изменяемы, а кортежи - нет. Это главное отличие."
            "JSON - это текстовый формат обмена данными, похожий на словари Python."
        ]
        answer = random.choice(answers)  # Случайный выбор

        # СОХРАНЕНИЕ В ИСТОРИЮ:
        # Формат {"role": "...", "content": "..."}  --> стандарт для OpenAI/Anthropic/API.
        # role = "user"                             --> сообщение от человека
        # role = "assistant"                        --> ответ от ИИ
        self.history.append({"role": "user", "content": question})
        self.history.append({"role": "assistant", "content": answer})

        return answer  # Отдаем ответ обратно в вызывающий код


    def get_history(self):
        """Простой геттер: возвращает весь список сообщений"""
        return self.history


    def get_stats(self):
        """Возвращает сводную статистику по агенту в виде словаря"""
        return {
            "name": self.name,  # Имя
            "message_count": len(self.history),  # Количество сообщений
            "last_check": datetime.now().strftime("%H:%M:%S")  # Время проверки
        }


