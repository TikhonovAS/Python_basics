# 1️⃣ ЦИКЛ for: перебор списка
fruits = ["яблоко", "банан", "вишня"]

for item in fruits:          # item поочерёдно принимает каждое значение
    print(f"Проверяю: {item}")
    if item == "банан":
        print("  → Нашёл банан! Останавливаемся.")
        break                # выходим из цикла полностью

# 🔍 Вывод:
# Проверяю: яблоко
# Проверяю: банан
#   → Нашёл банан! Останавливаемся.

# 2️⃣ ЦИКЛ for + range(): повтор N раз
for i in range(3):           # 0, 1, 2 (3 не включается!)
    print(f"Итерация №{i}")

# 3️⃣ ЦИКЛ while: повтор по условию
attempts = 0
while attempts < 3:
    code = input("Введите код (осталось 3 попытки): ")
    if code == "1234":
        print("Доступ разрешён ✅")
        break
    attempts += 1            # обязательно меняем условие, иначе бесконечный цикл!
else:
    print("Попытки исчерпаны. Блокировка 🔒")  # срабатывает, если не было break