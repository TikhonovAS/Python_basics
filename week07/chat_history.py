"""
Задача:
Напишите скрипт, который управляет историей диалога через JSON:
Функция load_history(filepath) → возвращает список сообщений из файла или [], если файла нет
Функция add_message(filepath, role, text) → добавляет {"role": role, "text": text} в историю и сохраняет файл
Функция get_last_n(filepath, n=2) → возвращает последние n сообщений
Функция clear_history(filepath) → очищает историю и записывает пустой список
В if __name__ == "__main__": протестируйте: создайте файл, добавьте 3 сообщения, выведите последние 2, очистите, проверьте результат.
"""
import json
import os

def load_history(filepath):
    """
    Загружает историю диалога из JSON-файла в память Python

    Если файл отсутствует на диске, возвращает пустой список [], чтобы избежать ошибки FileNotFoundError и позволить
    скрипту продолжить работу с "чистой" истороией.
    """
    # Шаг 1: Проверяем, лежит ли файл по указанному пути
    # если отсутствует, без этой проверки будет ошибка
    if os.path.exists(filepath):
        # Шаг 2: Файл ЕСТЬ -> открываем ТОЛЬКО для чтения ("r").
        # with гарантирует закрытие файла даже при ошибке чтения.
        with open(filepath, "r", encoding="utf=8") as f:
            # Шаг 3: json.load() читает текст файла и АВТОМАТИЧЕСКИ
            # превращает JSON-массив [...] в обычный Python-список [...]
            history = json.load(f)
        # Шаг 4: Отдаем готовый список тому, кто вызвал функцию
        return history
    else:
        # Шаг 5: Файла НЕТ -> не пытаемся читать
        # Возвращаем пустой список, как будто история только начинается
        return []


def add_message(filepath, role, text):
    """
    Добавляет новое сообщение в конце истории и сохраняет обновленный файл.

    Функция работает по принципу "загрузи -> измени в памяти -> сохрани целиком".
    Это необходимо, потому что JSON требует цельной структуры с открывающей и закрывающей
    квадратной скобкой. Простое добавление текста в конце файла (режим "а") нарушит валидность JSON

    :param filepath: Путь к JSON-файлу для сохранения истории
    :param role: Роль отправителя (например: 'user', 'assistant', 'sistem').
    :param text: Текстовое содержание сообщения.
    :return: None
    """
    # Шаг 1. Загружаем текущее состояние из файла (или []).
    # Теперь у нас живой Python-список в оперативной памяти.
    history = load_history(filepath)

    # Шаг 2. Создаем новое сообщение в виде словаря.
    new_message = {"role": role, "text": text}

    # Шаг 3. Добавляем сообщение в конец списка
    # Пока это происходит ТОЛЬКО в памяти Python. На диске файл еще старый
    history.append(new_message)

    # Шаг 4. Сохраняем обновленный список обратно в файл.
    # Открываем в режиме "w" (write/перезапись).
    # Мы НЕ используем "а" (append), потому что JSON - это цельная структура
    # Поэтому берем ВЕСЬ обновленный список и перезаписываем целиком.
    with open(filepath, "w", encoding='utf-8') as f:
        # json.dump() превращает Python-список обратно в JSON и записывает в файл.
        # indent=2    -> добавляет отступы для читаемости человеком.
        # ensure_ascii=False -> разрешает русские буквы
        json.dump(history, f, indent=2, ensure_ascii=False)


def get_last_n(filepath, n=2):
    """
    Возвращает последние n сообщений из файла истории.

    Использует Python-срезы, которые безопасно работают даже при запросе
    большого количества сообщений, чем есть в файле (вернет все, что доступно).

    :param filepath: Путь к JSON-файлу с историей.
    :param n: Количество последних сообщений для извлечения (по умолчанию 2).
    :return: Список последних n сообщений
    """
    # Шаг 1. Загружаем полную историю с диска в память
    history = load_history(filepath)

    # Шаг 2. Берем последние n элементов с помощью среза [start:end]
    # -n означает "начать с n-го элемента с конца"
    return history[-n:]


def clear_history(filepath):
    """
    Полностью очищает историю диалога, перезаписывает файл пустым JSON-массивом.

    Файл при этом не удаляется с диска. Это полезно, когда агент должен 'забыть'
    контент, но сохранить файл на том же пути для будущих записей.

    :param filepath: Путь к JSON-файлу, который нужно очистить
    :return: None
    """
    # Шаг 1. Открываем запись в режиме "w".
    # Если файл нет -> создастся. Если есть -> все содержимое удалиться.
    with open(filepath, "w", encoding="utf=8") as f:
        # Шаг 2. Записываем в него пустой JSON-массив []
        # Файл останется на диске, но внутри будет только пустая история.
        json.dump([], f, indent=2, ensure_ascii=False)


