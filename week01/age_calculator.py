# 1. Создаем переменную с текущим возрастом
current_age = 51

# 2. Считаем возраст через 10 лет
future_age = current_age + 10

# 3. Считаем возраст который был 5 лет назад
past_age = current_age - 5

# 4. Показываем результат
print("Сейчас мне:", current_age, "лет")
print("Через 10 лет мне будет:", future_age, "лет")
print("Пять лет назад мне было:", past_age, "лет")