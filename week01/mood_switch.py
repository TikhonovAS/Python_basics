# 1. Создаем "переключатель" - включено или выключено
is_happy = False

# 2. Выводим сообщение в зависимости от значения
print("Настроение:", is_happy)

# 3. Простая логика, если is_happy = True -> покажет смайлик, а если False - грустный
if is_happy:
    print(":) Все отлично!")
else:
    print(":( Все наладится!")